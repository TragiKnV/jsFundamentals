// Equal

'3' == 3;
console.log('3' == 3);

//JavaScript is helpful and does something known as coercion when comparing values
// Coercion is the process of converting a value from one datatype to another


// Strict Equal - overrides javascript coercion property. looks for datatypes

3 === 3;
console.log(3 === 3);

//Not Equal

console.log('3' != 3);

// Strict not equal

console.log('3' !== 3);

// Greater or Less than

3 > 2;
2 < 3;
console.log(3 > 2);
console.log(2 < 3);

// Greater than or equal to

3 >= 1;

//Less than or equal to

1 <= 3;  // less/greater than sign comes before the equal sign

// And

2 && 3;

// Or

2 || 3;